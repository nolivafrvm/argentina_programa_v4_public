package argentina_programa.claseUno;

public class TipoDatos {

    public static void main(String[] args) {
        // boolean Primitivo
        boolean unValorBooleano = true;
        // Char Primitivo
        char unaLetra = 'a';
        // Short Primitivo
        short shortNumber = 25;
        // Byte Primitivo
        byte byteNumber = 127;
        // int Primitivo
        int miPrimerContador = 66;
        // long Primitivo
        long idLong = 1L;
        // float Primitivo
        float otroNum = 2.344f;
        // double Primitivo
        double unValor = 1.68d;


        // Wrappers de datos

        Boolean unValorBooleanoWrapper = Boolean.TRUE;

        Character letter = new Character('a');

        Short shortNumberWrapper = new Short("25");

        Byte byteNumberWrapper = new Byte("127");

        Integer miContadorInteger = 66;

        Long idLongWrapper = 1L;

        Float otroNumberFloat = 2.352f;

        Double unValorDoble = 1.56d;
    }

}
